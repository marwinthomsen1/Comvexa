export function normalizeLanguage(language: unknown): DashboardLanguage {
  return languageOptions.includes(String(language)) ? (String(language) as DashboardLanguage) : "English";
}

export function getLanguageCode(language: string) {
  const codes: Record<DashboardLanguage, string> = {
    English: "en",
    Arabic: "ar",
    German: "de",
    French: "fr",
    Spanish: "es",
  };

  return codes[normalizeLanguage(language)];
}

export function getLanguageDirection(language: string) {
  normalizeLanguage(language);
  return "ltr";
}

function readLanguage() {
  try {
    const saved = window.localStorage.getItem("comvexa-workspace-settings");
    const settings = saved ? JSON.parse(saved) : null;
    return normalizeLanguage(settings?.language);
  } catch {
    return "English";
  }
}

export function useDashboardText() {
  const [language, setLanguage] = useState<DashboardLanguage>("English");

  useEffect(() => {
    function syncLanguage() {
      setLanguage(readLanguage());
    }

    syncLanguage();
    window.addEventListener("storage", syncLanguage);
    window.addEventListener("comvexa-settings-change", syncLanguage);

    return () => {
      window.removeEventListener("storage", syncLanguage);
      window.removeEventListener("comvexa-settings-change", syncLanguage);
    };
  }, []);

  const dictionary = dictionaries[language];

  return {
    language,
    dir: getLanguageDirection(language),
    text: dictionary,
    navLabel(label: string) {
      return dictionary.nav[label] ?? label;
    },
    groupLabel(label: string) {
      return dictionary.groups[label] ?? label;
    },
    translate(label: string) {
      return dictionary.common[label] ?? dictionary.nav[label] ?? label;
    },
  };
}
