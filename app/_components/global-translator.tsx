function readLanguage(): Language {
  try {
    const saved = window.localStorage.getItem("comvexa-workspace-settings");
    const settings = saved ? JSON.parse(saved) : null;
    const language = settings?.language ?? window.localStorage.getItem("comvexa-selected-language");

    if (language === "German" || language === "French" || language === "Spanish") {
      return language;
    }
  } catch {
    return "English";
  }

  return "English";
}

const originalText = new WeakMap<Text, string>();
const originalAttributes = new WeakMap<Element, Record<string, string>>();

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function translateText(value: string, dictionary: Record<string, string>) {
  const trimmed = value.trim();
  const normalized = trimmed.replace(/\s+/g, " ");
  const translated = dictionary[trimmed] ?? dictionary[normalized];

  if (translated) {
    return value.replace(trimmed, translated);
  }

  if (trimmed.length > 40) {
    return value;
  }

  let translatedParts = normalized;
  const entries = Object.entries(dictionary)
    .filter(([source]) => source.length > 2)
    .sort(([left], [right]) => right.length - left.length);

  entries.forEach(([source, target]) => {
    translatedParts = translatedParts.replace(new RegExp(escapeRegExp(source), "g"), target);
  });

  if (translatedParts === normalized) {
    return value;
  }

  return value.replace(trimmed, translatedParts);
}

function shouldSkipElement(element: Element | null) {
  if (!element) {
    return false;
  }

  if (element.closest("[data-no-translate]")) {
    return true;
  }

  return ["SCRIPT", "STYLE", "NOSCRIPT", "TEXTAREA", "OPTION"].includes(element.tagName);
}

function isLiveCurrencyText(value: string) {
  const normalized = value.trim().replace(/\s+/g, " ");

  return /^(USD|KWD|SAR|AED|QAR|BHD|OMR|EUR|GBP|SGD|\$|€|£)/.test(normalized);
}

function applyTranslations(language: Language) {
  const dictionary = language === "English" ? {} : common[language];
  document.documentElement.lang = languageCodes[language];
  document.documentElement.dir = "ltr";

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT);
  const nodes: Text[] = [];

  while (walker.nextNode()) {
    const node = walker.currentNode as Text;
    const parent = node.parentElement;

    if (!node.textContent?.trim() || shouldSkipElement(parent) || isLiveCurrencyText(node.textContent)) {
      continue;
    }

    nodes.push(node);
  }

  nodes.forEach((node) => {
    if (!originalText.has(node)) {
      originalText.set(node, node.textContent ?? "");
    }

    if (language === "English") {
      node.textContent = originalText.get(node) ?? node.textContent;
      return;
    }

    const source = originalText.get(node) ?? "";
    node.textContent = translateText(source, dictionary);
  });

  document.querySelectorAll("[placeholder], [aria-label], [title]").forEach((element) => {
    if (!originalAttributes.has(element)) {
      originalAttributes.set(element, {
        placeholder: element.getAttribute("placeholder") ?? "",
        ariaLabel: element.getAttribute("aria-label") ?? "",
        title: element.getAttribute("title") ?? "",
      });
    }

    const source = originalAttributes.get(element);

    if (!source) {
      return;
    }

    if (source.placeholder) {
      element.setAttribute("placeholder", language === "English" ? source.placeholder : translateText(source.placeholder, dictionary));
    }

    if (source.ariaLabel) {
      element.setAttribute("aria-label", language === "English" ? source.ariaLabel : translateText(source.ariaLabel, dictionary));
    }

    if (source.title) {
      element.setAttribute("title", language === "English" ? source.title : translateText(source.title, dictionary));
    }
  });
}

export function GlobalTranslator() {
  useEffect(() => {
    let frame = 0;

    function scheduleTranslate() {
      window.cancelAnimationFrame(frame);
      frame = window.requestAnimationFrame(() => applyTranslations(readLanguage()));
    }

    scheduleTranslate();

    const observer = new MutationObserver(scheduleTranslate);
    observer.observe(document.body, {
      childList: true,
      subtree: true,
      characterData: true,
      attributes: true,
      attributeFilter: ["placeholder", "aria-label", "title"],
    });

    window.addEventListener("storage", scheduleTranslate);
    window.addEventListener("comvexa-settings-change", scheduleTranslate);
    window.addEventListener("comvexa-language-change", scheduleTranslate);

    return () => {
      window.cancelAnimationFrame(frame);
      observer.disconnect();
      window.removeEventListener("storage", scheduleTranslate);
      window.removeEventListener("comvexa-settings-change", scheduleTranslate);
      window.removeEventListener("comvexa-language-change", scheduleTranslate);
    };
  }, []);

  return null;
}
